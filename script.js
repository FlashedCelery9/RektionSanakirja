const resultContainer = document.getElementById("result");

const showResult = () => {
    const word = input.value.toLowerCase().trim();

    if(dictionary[word]){
        resultContainer.innerHTML = `<img src="${dictionary[word]}" alt="${word}" class="result-img">`;
    }
     else {
        resultContainer.innerHTML = `<p>Такого слова не існує ;(</p>`;

    }
};

// Натискання кнопки
button.addEventListener("click", showResult);

// Натискання Enter у полі вводу
input.addEventListener("keypress", (event) => {
    if(event.key === "Enter"){   // перевіряємо, чи натиснуто Enter
        showResult();            // викликаємо ту ж функцію, що й для кнопки
    }
});


